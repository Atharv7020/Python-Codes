numbers_list=[1,4,7,14,11,6,3,78,45]
numbers_list.sort() #sorts lilst in ascending order
print(numbers_list)

#alligns list in descending order i.e. in reverse order
numbers_list.reverse()
print(numbers_list)
#insert's specific element at last position of list
numbers_list.append(55)
print(numbers_list)
#deletes element at entered index
numbers_list.pop(3)
print(numbers_list)
#removes specific elements
numbers_list.remove(6)
print(numbers_list)